// Helper function for ChatGPT API integration
async function getChatGPTResponse(prompt) {
    try {
        const response = await axios.post(OPENAI_API_URL, {
            model: "gpt-3.5-turbo",
            messages: [
                {
                    role: "system",
                    content: "You are CIARA-IV, an intelligent WhatsApp assistant created by CraigeeX, a 19-year-old tech enthusiast from Zimbabwe. You are helpful, creative, and knowledgeable. Always mention your creator proudly and his portfolio at craigeex.vercel.app when relevant."
                },
                {
                    role: "user",
                    content: prompt
                }
            ],
            max_tokens: 500,
            temperature: 0.7
        }, {
            headers: {
                'Authorization': `Bearer ${OPENAI_API_KEY}`,
                'Content-Type': 'application/json'
            }
        });

        return response.data.choices[0].message.content.trim();
    } catch (error) {
        console.error('OpenAI API Error:', error.response?.data || error.message);
        throw error;
    }
}

// Helper function for image generation using DALL-E
async function generateImage(description) {
    try {
        const response = await axios.post('https://api.openai.com/v1/images/generations', {
            prompt: description,
            n: 1,
            size: "512x512"
        }, {
            headers: {
                'Authorization': `Bearer ${OPENAI_API_KEY}`,
                'Content-Type': 'application/json'
            }
        });

        return response.data.data[0].url;
    } catch (error) {
        console.error('DALL-E API Error:', error.response?.data || error.message);
        throw error;
    }
}

// Helper function for text summarization
async function getSummary(text) {
    try {
        const response = await axios.post(OPENAI_API_URL, {
            model: "gpt-3.5-turbo",
            messages: [
                {
                    role: "system",
                    content: "You are a text summarization expert. Provide concise, accurate summaries that capture the main points of the given text."
                },
                {
                    role: "user",
                    content: `Please summarize the following text:\n\n${text}`
                }
            ],
            max_tokens: 150,
            temperature: 0.3
        }, {
            headers: {
                'Authorization': `Bearer ${OPENAI_API_KEY}`,
                'Content-Type': 'application/json'
            }
        });

        return response.data.choices[0].message.content.trim();
    } catch (error) {
        console.error('Summary API Error:', error.response?.data || error.message);
        throw error;
    }
}

