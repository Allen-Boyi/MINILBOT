// ChatGPT command
export const chatgpt = {
    name: 'chatgpt',
    aliases: ['gpt', 'ai', 'chat'],
    category: 'ai',
    description: 'Chat with AI',
    execute: async (sock, m, args) => {
        if (!args[0]) return m.reply('❌ Please provide a prompt!\n\nExample: .chatgpt What is artificial intelligence?');
        
        try {
            const prompt = args.join(' ');
            m.reply('🤖 AI is thinking...');
            
            const response = await getChatGPTResponse(prompt);
            
            const aiText = `*🤖 CIARA-IV AI RESPONSE*\n\n*Prompt:* ${prompt}\n\n*Response:*\n${response}\n\n_Powered by OpenAI GPT_`;
            
            await sock.sendMessage(m.chat, { text: aiText }, { quoted: m });
            
        } catch (error) {
            console.error('ChatGPT Error:', error.response?.data || error.message);
            m.reply('❌ AI service is currently unavailable. Please try again later.');
        }
    }
};

