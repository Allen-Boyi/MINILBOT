// Text summarization
export const summary = {
    name: 'summary',
    aliases: ['summarize', 'tldr'],
    category: 'ai',
    description: 'Summarize text using AI',
    execute: async (sock, m, args) => {
        if (!args[0] && !m.quoted) return m.reply('❌ Please provide text to summarize!\n\nExample: .summary [long text] or reply to a message');
        
        try {
            const text = args.join(' ') || m.quoted?.text || '';
            if (text.length < 50) return m.reply('❌ Text too short to summarize!');
            
            m.reply('📄 AI is analyzing and summarizing...');
            
            const summary = await getSummary(text);
            
            const summaryText = `*📄 AI TEXT SUMMARY*\n\n*Original Length:* ${text.length} characters\n*Summary Length:* ${summary.length} characters\n*Compression:* ${Math.round((1 - summary.length/text.length) * 100)}%\n\n*📝 Summary:*\n${summary}\n\n_Summarized by CIARA-IV AI_`;
            
            await sock.sendMessage(m.chat, { text: summaryText }, { quoted: m });
            
        } catch (error) {
            console.error('Summary Error:', error.response?.data || error.message);
            m.reply('❌ Summarization failed. Please try again.');
        }
    }
};

